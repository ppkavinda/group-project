    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $posts = auth()->user()->posts;
        $courses = [];

        foreach ($posts as $post) {
            if (in_array($post->course, $courses)) {
                continue;
            }
            array_push($courses, $post->course);
        }

        $enrolled = [];

        foreach ($courses as $course) {
            array_push($enrolled, [$course->title => $course->users]);
        }

        return view('enrolledUsers', compact('enrolled'));
    }

